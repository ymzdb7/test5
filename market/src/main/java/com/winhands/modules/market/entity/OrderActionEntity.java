package com.winhands.modules.market.entity;

import java.io.Serializable;
import java.security.Timestamp;

public class OrderActionEntity implements Serializable{

	/**
	 * 订单操作管理
	 */
	private static final long serialVersionUID = 1L;
	private Integer id;//商品id
	private Integer  actionId;//流水号
	private Integer orderId;//被操作的交易号
	private String actionUser;//操作该次的人员
	private Integer orderStatus;//订单状态1.未付款2.已付款待提货3.已提货4.退货5.订单取消6.其它
	private String actionNote;//操作备注
	private Integer logTime;//操作时间
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getActionId() {
		return actionId;
	}
	public void setActionId(Integer actionId) {
		this.actionId = actionId;
	}
	public Integer getOrderId() {
		return orderId;
	}
	public void setOrderId(Integer orderId) {
		this.orderId = orderId;
	}
	public String getActionUser() {
		return actionUser;
	}
	public void setActionUser(String actionUser) {
		this.actionUser = actionUser;
	}
	public Integer getOrderStatus() {
		return orderStatus;
	}
	public void setOrderStatus(Integer orderStatus) {
		this.orderStatus = orderStatus;
	}
	public String getActionNote() {
		return actionNote;
	}
	public void setActionNote(String actionNote) {
		this.actionNote = actionNote;
	}
	public Integer getLogTime() {
		return logTime;
	}
	public void setLogTime(Integer logTime) {
		this.logTime = logTime;
	}
	
	
}
